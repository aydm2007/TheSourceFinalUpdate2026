def run_cmd(cmd, cwd=None):
    start = time.time()
    try:
        result = subprocess.run(cmd, shell=True, capture_output=True, text=True, cwd=cwd)
        latency = (time.time() - start) * 1000
        return result.returncode, result.stdout, result.stderr, latency
    except Exception as e:
        return -1, "", str(e), 0

def native_secret_search(root_path):
    """Deep Python-native search for secrets to avoid platform dependency (Grep)."""
    leaks = []
    pattern = re.compile(r"sk-[A-Za-z0-9]{20,}")
    for root, dirs, files in os.walk(root_path):
        if ".git" in root or ".venv" in root: continue
        for file in files:
            if file.endswith((".py", ".env", ".txt", ".js", ".json")):
                f_path = os.path.join(root, file)
                try:
                    with open(f_path, "r", encoding="utf-8", errors="ignore") as f:
                        content = f.read()
                        if pattern.search(content):
                            leaks.append(f_path)
                except:
                    pass
    return leaks

