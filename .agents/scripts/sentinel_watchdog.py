def log_event(event_type, message, status="INFO"):
    entry = {
        "timestamp": datetime.datetime.utcnow().isoformat() + "Z",
        "type": f"OMEGA_SENTINEL_{event_type}",
        "status": status,
        "message": message,
        "sovereign_version": "15.0-Omega-AutoPilot"
    }
    with open(LEDGER_PATH, "a", encoding="utf-8") as f:
        f.write(json.dumps(entry) + "\n")

